                        <h2>Influência dos Imigrantes</h2>
                        <p>A culinária de São Roque é bastante diversa e influenciada por diferentes culturas e tradições, incluindo a culinária de outros países. A cidade recebeu ao longo de sua história diversas influências gastronômicas, tanto de outros estados do Brasil quanto de países como Itália, Portugal e Japão.</p>
                        <p>A influência italiana é bastante presente na culinária de São Roque, especialmente na produção de vinhos. Muitas vinícolas da região foram fundadas por imigrantes italianos, que trouxeram suas técnicas de cultivo e produção de vinhos para o Brasil. Além disso, a pizza e a massa são pratos bastante populares na região, e muitos restaurantes italianos podem ser encontrados em São Roque.</p>
                        <p>A influência portuguesa também é presente na culinária de São Roque, especialmente na produção de queijos e na criação de doces tradicionais, como o pastel de Santa Clara. Além disso, o famoso pão de alho de São Roque também tem origem portuguesa, sendo uma variação do pão de alho português.</p>
                        <p>A influência japonesa, por sua vez, pode ser vista na presença de pratos como sushi e sashimi em diversos restaurantes da cidade. A culinária japonesa tem se tornado cada vez mais popular no Brasil, e São Roque não é exceção.</p>
                        <p>A culinária de São Roque é uma mistura de diferentes tradições e culturas, que se fundem para criar pratos únicos e saborosos. A influência de outros países é apenas uma das muitas facetas dessa rica culinária, que se mantém viva e vibrante ao longo dos anos.</p>
